const LOCALE_MAP = { en: "en-US", es: "es-ES", fr: "fr-FR" };
const LANGUAGE_NAMES = { en: "English", es: "Español", fr: "Français" };

let currentLang = localStorage.getItem("marginNotesLang") || "en";
if (!TRANSLATIONS[currentLang]) currentLang = "en";

function t(key, vars) {
  let str = (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) || TRANSLATIONS.en[key] || key;
  if (vars) {
    for (const k in vars) str = str.replace(`{${k}}`, vars[k]);
  }
  return str;
}

function currentLocale() {
  return LOCALE_MAP[currentLang] || "en-US";
}

function formatDateLabel(iso) {
  return new Date(iso + "T00:00:00Z").toLocaleDateString(currentLocale(), {
    month: "short",
    day: "numeric",
    year: "numeric",
    timeZone: "UTC",
  });
}

function applyStaticTranslations(root) {
  (root || document).querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  (root || document).querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.getAttribute("data-i18n-placeholder"));
  });
}

function setLang(lang) {
  currentLang = TRANSLATIONS[lang] ? lang : "en";
  localStorage.setItem("marginNotesLang", currentLang);
  document.documentElement.lang = currentLang;
  applyStaticTranslations();
}

document.documentElement.lang = currentLang;
applyStaticTranslations();
